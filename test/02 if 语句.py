# -*- coding=utf-8 -*-

# if 条件语句:
#     条件成立时执行的代码
# else:
#     条件不成立执行的代码

# 条件语句： 表达式、变量...... 本质它都会被转成一个布尔值，如果是True就表示条件成立，如果是False 就表示条件不成立。
# 数据是分真和假的： True False

"""
    整数：数字0是假
    浮点数：0.0 是假
    布尔：False 是假
    字符串：空字符串是假
    字典：空字典是假的
    列表：空列表是假的
    元组：空元组是假的
    None 是假的

    不同的数据类型之间是可以转换的
"""

a = 1

if a:
    print( "这是真的" )
else:
    print( "这是假的" )

